package br.com.construtora.model;

public class Obra {
	private int idObra;
	private String siglaObra;
	private String localObra;
	
	
	public int getIdObra() {
		return idObra;
	}
	public void setIdObra(int idObra) {
		this.idObra = idObra;
	}
	public String getSiglaObra() {
		return siglaObra;
	}
	public void setSiglaObra(String siglaObra) {
		this.siglaObra = siglaObra;
	}
	public String getLocalObra() {
		return localObra;
	}
	public void setLocalObra(String localObra) {
		this.localObra = localObra;
	}
	
	

}
